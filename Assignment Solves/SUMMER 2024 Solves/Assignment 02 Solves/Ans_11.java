public class Ans_11 {
    public static void main(String[] args) {
        float a = 4.5f, b = 9.5f ;
        double c = Math.sqrt(a*a + b*b) ;
        System.out.println("Sin(A) = " +a/c);
        System.out.println("Cos(A) = " +b/c);
        System.out.println("Sin(B) = " +b/c);
        System.out.println("Cos(B) = " +a/c);
    }
}
